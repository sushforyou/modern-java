public class AccessModifiers {
    public static void main(String[] args) {
        System.out.println("Access Modifiers in Java:");
        System.out.println("1. public: Accessible from anywhere.");
        System.out.println("2. private: Accessible only within the class.");
        System.out.println("3. protected: Accessible within the package and subclasses.");
        System.out.println("4. default (no modifier): Accessible within the package.");

        
    }
}
